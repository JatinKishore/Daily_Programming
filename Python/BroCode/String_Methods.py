name = "bro code"
print(len(name)) # length

print(name.find("b")) # to find first occurence of a character

print(name.capitalize()) # to capitalize the first letter in first word


print(name.upper()) # upperCase all the letters

print(name.lower()) # lowerCase all the letters

print(name.isdigit()) # check string contains only numbers

print(name.isalpha()) # check string contains only alphabets

print(name.count("o")) # check for numbers of occurence of a specific character

print(name.replace("o","a")) # to replace a character with another character

print(name*3) # it is not a method but used to print a string n times


